#!/usr/bin/env node

import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const workerDir = path.resolve(__dirname, "..");
const repoRoot = path.resolve(workerDir, "..");

const directivesPath = path.join(repoRoot, "directives.yml");
const directivesSigPath = path.join(repoRoot, "directives.yml.sig");
const bundledPath = path.join(workerDir, "src", "bundled.ts");

function readRequiredFile(filePath) {
  try {
    return readFileSync(filePath, "utf8");
  } catch (err) {
    const msg = err instanceof Error ? err.message : String(err);
    console.error(`[embed-directives] failed to read ${filePath}: ${msg}`);
    process.exit(1);
  }
}

const directivesYaml = readRequiredFile(directivesPath);
const directivesSig = readRequiredFile(directivesSigPath);

const generated = `// AUTO-GENERATED by worker/scripts/embed-directives.mjs.
// Edit ../directives.yml and ../directives.yml.sig instead.

export const DIRECTIVES_YAML = ${JSON.stringify(directivesYaml)};

export const DIRECTIVES_SIG = ${JSON.stringify(directivesSig)};
`;

writeFileSync(bundledPath, generated, "utf8");
console.log(`[embed-directives] wrote ${path.relative(workerDir, bundledPath)}`);
